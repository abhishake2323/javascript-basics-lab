// 1.	Use Basic Operators

// •	Perform and log operations using different types of operators:
// •	Arithmetic Operators: Addition (+), Subtraction (-), Multiplication (*), and Division (/).

let num1 = 20 
let num2 = 10

console.log(`Addition of ${num1} , ${num2} :`, num1+num2)
console.log(`Subtraction of ${num1} , ${num2} :`, num1-num2)
console.log(`Multiplication of ${num1} , ${num2} :`, num1*num2)
console.log(`Division of  ${num1} , ${num2} :`, num1/num2)


// •	Comparison Operators: Greater than (>), Less than (<), Equal to (===), and Not equal (!==).

console.log(`Is  ${num1} greater than ${num2} :`, num1>num2)
console.log(`Is  ${num1} less than  ${num2} :`, num1<num2)
console.log(`Is ${num1} equal to ${num2} :`, num1===num2)
console.log(`Is ${num1} Not equal to ${num2} :`, num1!==num2)

// •	Logical Operators: AND (&&), OR (||), and NOT (!).
let bool1 = true
let bool2 = false
console.log(`Logical AND ( && ) between ${bool1} and ${bool2} is `, bool1&&bool2)
console.log(`Logical OR ( || ) between ${bool1} and ${bool2} is `, bool1||bool2)
console.log(`Logical NOT (! ) on ${bool1} is `, !bool1)
console.log(`Logical NOT (! ) on ${bool2} is `, !bool2)


